import { chromium } from 'playwright';
const browser = await chromium.launch({ headless: true });
const page = await browser.newPage();
const logs = [];
page.on('console', m => logs.push(`[${m.type()}] ${m.text()}`));
page.on('pageerror', e => logs.push(`[pageerror] ${e.message}`));
await page.goto('https://the-bricks-site-052026.web.app/projects', { waitUntil: 'domcontentloaded', timeout: 60000 });
await page.waitForTimeout(8000);
const before = page.url();
await page.click('a.project-item[data-id="hameedia_husn_eid_campaign"]', { timeout: 15000 }).catch(e => logs.push('click fail: ' + e.message));
await page.waitForTimeout(10000);
const after = page.url();
const hasUi = await page.$('#ui');
const hasProjectDetails = await page.$('#project-details');
const preloaderVisible = await page.evaluate(() => { const p = document.getElementById('preloader'); return p ? getComputedStyle(p).display !== 'none' && p.offsetParent !== null : false; });
console.log('URL before:', before);
console.log('URL after:', after);
console.log('hasUi:', !!hasUi, 'hasProjectDetails:', !!hasProjectDetails, 'preloaderVisible:', preloaderVisible);
console.log('TITLE:', await page.title());
logs.slice(-30).forEach(l => console.log(l));
await browser.close();
